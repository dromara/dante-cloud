CREATE TABLE config_info (
    id NUMBER(20) GENERATED BY DEFAULT AS IDENTITY,
    data_id VARCHAR2(255) NOT NULL,
    group_id VARCHAR2(128) NOT NULL,
    tenant_id VARCHAR2(128) DEFAULT '',
    app_name VARCHAR2(128),
    content CLOB,
    md5 VARCHAR2(32) DEFAULT NULL,
    gmt_create TIMESTAMP NOT NULL DEFAULT SYSTIMESTAMP,
    gmt_modified TIMESTAMP NOT NULL DEFAULT SYSTIMESTAMP,
    src_user VARCHAR2(128) DEFAULT NULL,
    src_ip VARCHAR2(50) DEFAULT NULL,
    c_desc VARCHAR2(256) DEFAULT NULL,
    c_use VARCHAR2(64) DEFAULT NULL,
    effect VARCHAR2(64) DEFAULT NULL,
    type VARCHAR2(64) DEFAULT NULL,
    c_schema CLOB DEFAULT NULL,
    encrypted_data_key VARCHAR2(1024) DEFAULT NULL,
    CONSTRAINT configinfo_id_key PRIMARY KEY (id),
    CONSTRAINT uk_configinfo_datagrouptenant UNIQUE (data_id, group_id, tenant_id)
);

CREATE INDEX configinfo_dataid_key_idx ON config_info(data_id);
CREATE INDEX configinfo_groupid_key_idx ON config_info(group_id);
CREATE INDEX configinfo_dataid_group_key_idx ON config_info(data_id, group_id);

CREATE TABLE his_config_info (
    id NUMBER(20) NOT NULL,
    nid NUMBER(20) GENERATED BY DEFAULT AS IDENTITY,
    data_id VARCHAR2(255) NOT NULL,
    group_id VARCHAR2(128) NOT NULL,
    tenant_id VARCHAR2(128) DEFAULT '',
    app_name VARCHAR2(128),
    content CLOB,
    md5 VARCHAR2(32) DEFAULT NULL,
    gmt_create TIMESTAMP NOT NULL DEFAULT SYSTIMESTAMP,
    gmt_modified TIMESTAMP NOT NULL DEFAULT SYSTIMESTAMP,
    src_user VARCHAR2(128),
    src_ip VARCHAR2(50) DEFAULT NULL,
    publish_type VARCHAR2(50) DEFAULT 'formal',
    gray_name VARCHAR2(128) DEFAULT NULL,
    ext_info CLOB,
    op_type CHAR(10) DEFAULT NULL,
    encrypted_data_key VARCHAR2(1024) DEFAULT NULL,
    CONSTRAINT hisconfiginfo_nid_key PRIMARY KEY (nid)
);

CREATE INDEX hisconfiginfo_dataid_key_idx ON his_config_info(data_id);
CREATE INDEX hisconfiginfo_gmt_create_idx ON his_config_info(gmt_create);
CREATE INDEX hisconfiginfo_gmt_modified_idx ON his_config_info(gmt_modified);

CREATE TABLE config_info_gray (
    id NUMBER(20) GENERATED BY DEFAULT AS IDENTITY,
    data_id VARCHAR2(255) NOT NULL,
    group_id VARCHAR2(128) NOT NULL,
    tenant_id VARCHAR2(128) DEFAULT '',
    gray_name VARCHAR2(128) NOT NULL,
    gray_rule CLOB,
    app_name VARCHAR2(128),
    src_ip VARCHAR2(128),
    src_user VARCHAR2(128) DEFAULT '',
    content CLOB,
    md5 VARCHAR2(32) DEFAULT NULL,
    encrypted_data_key VARCHAR2(256) DEFAULT NULL,
    gmt_create TIMESTAMP NOT NULL DEFAULT SYSTIMESTAMP,
    gmt_modified TIMESTAMP NOT NULL DEFAULT SYSTIMESTAMP,
    CONSTRAINT configinfogray_id_key PRIMARY KEY (id),
    CONSTRAINT uk_configinfogray_datagrouptenantgrayname UNIQUE (data_id, group_id, tenant_id, gray_name)
);

CREATE INDEX config_info_gray_dataid_gmt_modified ON config_info_gray(data_id, gmt_modified);
CREATE INDEX config_info_gray_gmt_modified ON config_info_gray(gmt_modified);

CREATE TABLE config_tags_relation (
    id NUMBER(19) NOT NULL,
    tag_name VARCHAR2(128) NOT NULL,
    tag_type VARCHAR2(64) DEFAULT NULL,
    data_id VARCHAR2(255) NOT NULL,
    group_id VARCHAR2(128) NOT NULL,
    tenant_id VARCHAR2(128) DEFAULT '',
    nid NUMBER(20) GENERATED BY DEFAULT AS IDENTITY,
    CONSTRAINT config_tags_id_key PRIMARY KEY (nid),
    CONSTRAINT uk_configtagrelation_configidtag UNIQUE (id, tag_name, tag_type)
