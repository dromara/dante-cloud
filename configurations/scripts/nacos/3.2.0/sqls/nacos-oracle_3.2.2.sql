CREATE TABLE pipeline_execution (
    execution_id  VARCHAR2(64)  NOT NULL,
    resource_type VARCHAR2(32)  NOT NULL,
    resource_name VARCHAR2(256) NOT NULL,
    namespace_id  VARCHAR2(128) DEFAULT NULL,
    version       VARCHAR2(64)  DEFAULT NULL,
    status        VARCHAR2(32)  NOT NULL,
    pipeline      CLOB          NOT NULL,
    create_time   NUMBER(20)    NOT NULL,
    update_time   NUMBER(20)    NOT NULL,
    CONSTRAINT pk_pipeline_execution PRIMARY KEY (execution_id)
);

/******************************************/
/*   表名称 = ai_resource                 */
/******************************************/
CREATE TABLE ai_resource (
    id NUMBER(20) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    gmt_create TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    gmt_modified TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    name VARCHAR2(256) NOT NULL,
    type VARCHAR2(32) NOT NULL,
    c_desc VARCHAR2(1024) DEFAULT NULL,
    status VARCHAR2(32) DEFAULT NULL,
    namespace_id VARCHAR2(128) DEFAULT '' NOT NULL,
    biz_tags VARCHAR2(1024) DEFAULT NULL,
    ext CLOB DEFAULT NULL,
    c_from VARCHAR2(256) DEFAULT 'local' NOT NULL,
    version_info CLOB DEFAULT NULL,
    meta_version NUMBER(20) DEFAULT 1 NOT NULL,
    scope VARCHAR2(16) DEFAULT 'PRIVATE' NOT NULL,
    owner VARCHAR2(128) DEFAULT '' NOT NULL,
    download_count NUMBER(20) DEFAULT 0 NOT NULL,
    CONSTRAINT uk_ai_resource_ns_name_type UNIQUE (namespace_id, name, type, c_from)
);

CREATE INDEX idx_ai_resource_name ON ai_resource(name);
CREATE INDEX idx_ai_resource_type ON ai_resource(type);
CREATE INDEX idx_ai_resource_gmt_modified ON ai_resource(gmt_modified);

/******************************************/
/*   表名称 = ai_resource_version         */
/******************************************/
CREATE TABLE ai_resource_version (
    id NUMBER(20) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    gmt_create TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    gmt_modified TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    type VARCHAR2(32) NOT NULL,
    author VARCHAR2(128) DEFAULT NULL,
    name VARCHAR2(256) NOT NULL,
    c_desc VARCHAR2(1024) DEFAULT NULL,
    status VARCHAR2(32) NOT NULL,
    version VARCHAR2(64) NOT NULL,
    namespace_id VARCHAR2(128) DEFAULT '' NOT NULL,
    storage CLOB DEFAULT NULL,
    publish_pipeline_info CLOB DEFAULT NULL,
    download_count NUMBER(20) DEFAULT 0 NOT NULL,
    CONSTRAINT uk_ai_res_ver_ns_nm_tp_ver UNIQUE (namespace_id, name, type, version)
);

CREATE INDEX idx_ai_res_ver_name ON ai_resource_version(name);
CREATE INDEX idx_ai_res_ver_status ON ai_resource_version(status);
CREATE INDEX idx_ai_res_ver_gmt_mod ON ai_resource_version(gmt_modified);
